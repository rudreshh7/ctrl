// Get total emoji count
export function getTotalEmojiCount() {
  return Object.values(emojiCategories).reduce(
    (total, category) => total + category.emojis.length,
    0
  );
}

// Get all emojis in a flat array
export function getAllEmojis() {
  const allEmojis = [];
  Object.values(emojiCategories).forEach((category) => {
    allEmojis.push(...category.emojis);
  });
  return allEmojis;
}

// Get emojis by category
export function getEmojisByCategory(categoryKey) {
  return emojiCategories[categoryKey]?.emojis || [];
}

// Search emojis by query
export function searchEmojis(query) {
  const lowerQuery = query.toLowerCase();
  const results = [];

  Object.values(emojiCategories).forEach((category) => {
    category.emojis.forEach((emojiData) => {
      const searchText = `${emojiData.name} ${emojiData.keywords.join(
        " "
      )}`.toLowerCase();

      if (searchText.includes(lowerQuery)) {
        results.push({
          ...emojiData,
          score: searchText.indexOf(lowerQuery) / searchText.length,
        });
      }
    });
  });

  return results.sort((a, b) => a.score - b.score);
}
